    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        StringBuilder add_nha_cung_cap = new StringBuilder();
        if(jTextField1.getText().equals("")) {
            add_nha_cung_cap.append("Thong Tin Chua Day Du!");
            jTextField1.setBackground(Color.RED);
        }
        else {
            jTextField1.setBackground(Color.white);
        }
        
        if(jTextField2.getText().equals("")) {
            add_nha_cung_cap.append("Thong Tin Chua Day Du!");
            jTextField2.setBackground(Color.RED);
        }
        else {
            jTextField2.setBackground(Color.white);
        }
        
        if(jTextField3.getText().equals("")) {
            add_nha_cung_cap.append("Thong Tin Chua Day Du!");
            jTextField3.setBackground(Color.RED);
        }
        else {
            jTextField3.setBackground(Color.white);
        }
        
        if(jTextField4.getText().equals("")) {
            add_nha_cung_cap.append("Thong Tin Chua Day Du!");
            jTextField4.setBackground(Color.RED);
        }
        else {
            jTextField4.setBackground(Color.white);
        }
        
        if(add_nha_cung_cap.length() > 0) {
            JOptionPane.showMessageDialog(this, add_nha_cung_cap);
            return ;
        }
        
        try {
            timkiemNCC tk = new timkiemNCC();
            NhaCungCap ncc = new NhaCungCap();
            NhaCungCap ncc1 = tk.timkiem_ncc(jTextField4.getText());
            ncc.setMaNCC(jTextField4.getText());
            ncc.setTenNCC(jTextField1.getText());
            ncc.setDiaChi(jTextField2.getText());
            ncc.setDienThoai(jTextField3.getText());
            
            insert_NCC insert_ncc = new insert_NCC();
            if(ncc1 != null) {
                JOptionPane.showMessageDialog(this, "Ma Nha Cung Cap Nay Da Ton Tai!");
                return;
            }
            
            insert_ncc.insertNhaCungCap(ncc);
            JOptionPane.showMessageDialog(this, "Da Them Thanh Cong Nha Cung Cap!");
            LoadDatabase();
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

