    public void LoadDatabase(){
            
            try {
                    String insert = "select * from nhacungcap";
                    Connection connect = database_connect.connection();
                    PreparedStatement data_connect = connect.prepareStatement(insert);
                    
                    ResultSet rs = data_connect.executeQuery();
                    tblModel.setRowCount(0);
                    while(rs.next()) {
                        String[] row = new String[]{rs.getString("mancc"), rs.getString("tenncc"), rs.getString("diachi"), rs.getString("dienthoai")};
                        tblModel.addRow(row);
                    }
                    tblModel.fireTableDataChanged();
            
            }
                
            catch(Exception e) {
            
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
    }

