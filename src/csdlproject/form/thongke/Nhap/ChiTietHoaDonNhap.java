    private void ThemButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ThemButtonActionPerformed
        // TODO add your handling code here:
        if(MHText.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm");
            return;
        }
            String[] row = new String[]{MHText.getText() ,TenHang.getText(), DonGia.getText(), SoLuong.getText(), Tien.getText()};
            tblModel.addRow(row);
            Tong += Integer.parseInt(Tien.getText());
            TongGIaTri.setText(Integer.toString(Tong));
    }//GEN-LAST:event_ThemButtonActionPerformed

    private void MaNCCComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MaNCCComboBoxActionPerformed
        // TODO add your handling code here:
        String s = MaNCCComboBox.getSelectedItem().toString();
        if(s != "Chon"){
            try{
               String sql = "select tenncc, diachi, dienthoai from nhacungcap where mancc = ?";
               Connection connect = database_connect.connection();
               PreparedStatement data_connect = connect.prepareStatement(sql);
               data_connect.setString(1, s);
               ResultSet rs = data_connect.executeQuery();
               if(rs.next()){
                  TenNCCText.setText(rs.getString("tenncc"));
                  DiaChi.setText(rs.getString("diachi"));
                  DienThoai.setText(rs.getString("dienthoai"));
               }
            }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }       
        }
        else{
            TenNCCText.setText("");
            DiaChi.setText("");
            DienThoai.setText("");
        }
    }//GEN-LAST:event_MaNCCComboBoxActionPerformed

