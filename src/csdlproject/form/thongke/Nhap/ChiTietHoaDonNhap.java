    public void loadMaNCCCombobox(){
        try{
            String sql = "select mancc from nhacungcap";
            Connection connect = database_connect.connection();
            PreparedStatement data_connect = connect.prepareStatement(sql);
            ResultSet rs = data_connect.executeQuery();
            while(rs.next()){
               String s = rs.getString(1);
               MaNCCComboBox.addItem(s);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
   
